/**
 --------------------------
 **Installing Express.js
 --------------------------
 * 1. Create a folder (mkdir or manually)
 * 2. cd that folder
 * 3. npm install express
 * 4. create index.js into code blocks

 ---------------------------
 **For express or node server
 ---------------------------
 * 1. require express
 * 2. create app variable
 * 3. declare port
 * 4. set app.get('/)
 * 5. listen to port
 * 6. npm install cors (from server side)
 * 7. require cors
 * 8. app.use(cors())

 -----------------------------
 **Install nodemon for auto run
 ------------------------------
 * 1. npm install -g nodemon (for one time)
 * 2. write two line script into the package.json
 ** "start": "node index.js",
 ** "start": "nodemon index.js",
 ** then, type in command line(nodemon index.js) for auto run
 *
*/